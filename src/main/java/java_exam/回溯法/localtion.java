package java_exam.回溯法;

public class localtion {
    public int x,y;
    public int getX() {
        return x;
    }

    public localtion(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getY() {
        return y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }
}
